class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        i = 0
        j = len(count) - 1
        left = 0
        right = 0
        mode_cnt = 0
        mode = 0
        sumall = 0
        minimum = -1
        maximum = -1
        mean = 0
        median = 0
        while i < j:
            if left <= right:
                if count[i]:
                    left += count[i]
                    sumall += count[i] * i
                    if mode_cnt < count[i]:
                        mode_cnt = count[i]
                        mode = i
                    if minimum == -1:
                        minimum = i
                i += 1
            else:
                if count[j]:
                    right += count[j]
                    sumall += count[j] * j
                    if mode_cnt < count[j]:
                        mode_cnt = count[j]
                        mode = j
                    if maximum == -1:
                        maximum = j
                j -= 1
        # now i == j
        print(left,right,i,j)

        if count[i]:
            sumall += count[i] * i
            mean = sumall / (left + right + count[i])
            if left == right:
                median = i
            else:
                if left < right:
                    left += count[i]
                    j += 1
                else:
                    right += count[j]
                    i -= 1
                    print(left, right, i, j)

                if left < right:
                    median = i + 1
                elif left > right:
                    median = j - 1
                else:
                    median = (i + j) / 2
        else:
            mean = sumall / (left + right)
            if left < right:
                median = j + 1
            elif left > right:
                median = i - 1
            else:
                median = i
        print(minimum / 1.0, maximum / 1.0, mean / 1.0, median / 1.0, mode / 1.0)


