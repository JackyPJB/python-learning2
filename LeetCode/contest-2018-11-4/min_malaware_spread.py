def get_num_infect(graph, initial):
    infected = copy.deepcopy(initial)
    initial2 = copy.deepcopy(initial)
    while initial2:
        k = initial2.pop()
        for x, y in enumerate(graph[k]):
            if y == 1:
                if x not in infected:
                    initial2.append(x)
                    infected.append(x)
    return len(infected)


def min_malware_spread2(graph, initial):
    n = len(graph)
    m = n
    res = 0
    for i in sorted(initial):
        graph_tmp = copy.deepcopy(graph)
        initial_tmp = copy.copy(initial)
        for s in graph_tmp:
            s[i] = 0
        if i in initial_tmp:
            initial_tmp.remove(i)
        num = get_num_infect(graph_tmp, initial_tmp)
        if num < m:
            m = num
            res = i
    return [res, m]


