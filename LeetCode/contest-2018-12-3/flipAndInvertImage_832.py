import time

__author__ = 'Max_Pengjb'
start = time.time()


# 下面写上代码块
class Solution(object):
    def flipAndInvertImage(self, A):
        """
        :type A: List[List[int]]
        :rtype: List[List[int]]
        """
        for a in A:
            i = 0
            j = len(A) - 1
            while i <= j:
                if a[i] == a[j]:
                    a[i] = a[j] = a[i] ^ 1
                i += 1
                j -= 1
        return A


A_in = [[1, 1, 0, 0], [1, 0, 0, 1], [0, 1, 1, 1], [1, 0, 1, 0]]
res = Solution().flipAndInvertImage(A_in)
print(res)
